  @doc """
  Dispatches a drag event into the page.
  ## Parameters:
    - (Required) `type`: Type of the drag event.
  - (Required) `x`: X coordinate of the event relative to the main frame's viewport in CSS pixels.
  - (Required) `y`: Y coordinate of the event relative to the main frame's viewport in CSS pixels. 0 refers to
  the top of the viewport and Y increases as it proceeds towards the bottom of the viewport.
  - (Required) `data`: description not provided :(
  - (Optional) `modifiers`: Bit field representing pressed modifier keys. Alt=1, Ctrl=2, Meta/Command=4, Shift=8
  (default: 0).
  """
  @spec dispatch_drag_event(
          String.t(),
          number(),
          number(),
          CDPotion.Domain.Input.DragData,
          integer()
        ) :: {String.t(), map()}
  def dispatch_drag_event(type, x, y, data, modifiers \\ nil) do
    params =
      as_query([{"type", type}, {"x", x}, {"y", y}, {"data", data}, {"modifiers", modifiers}])

    {"Input.dispatchDragEvent", params}
  end

  @doc """
  Dispatches a key event to the page.
  ## Parameters:
    - (Required) `type`: Type of the key event.
  - (Optional) `modifiers`: Bit field representing pressed modifier keys. Alt=1, Ctrl=2, Meta/Command=4, Shift=8
  (default: 0).
  - (Optional) `timestamp`: Time at which the event occurred.
  - (Optional) `text`: Text as generated by processing a virtual key code with a keyboard layout. Not needed for
  for `keyUp` and `rawKeyDown` events (default: "")
  - (Optional) `unmodified_text`: Text that would have been generated by the keyboard if no modifiers were pressed (except for
  shift). Useful for shortcut (accelerator) key handling (default: "").
  - (Optional) `key_identifier`: Unique key identifier (e.g., 'U+0041') (default: "").
  - (Optional) `code`: Unique DOM defined string value for each physical key (e.g., 'KeyA') (default: "").
  - (Optional) `key`: Unique DOM defined string value describing the meaning of the key in the context of active
  modifiers, keyboard layout, etc (e.g., 'AltGr') (default: "").
  - (Optional) `windows_virtual_key_code`: Windows virtual key code (default: 0).
  - (Optional) `native_virtual_key_code`: Native virtual key code (default: 0).
  - (Optional) `auto_repeat`: Whether the event was generated from auto repeat (default: false).
  - (Optional) `is_keypad`: Whether the event was generated from the keypad (default: false).
  - (Optional) `is_system_key`: Whether the event was a system key event (default: false).
  - (Optional) `location`: Whether the event was from the left or right side of the keyboard. 1=Left, 2=Right (default:
  0).
  - (Optional) `commands`: Editing commands to send with the key event (e.g., 'selectAll') (default: []).
  These are related to but not equal the command names used in `document.execCommand` and NSStandardKeyBindingResponding.
  See https://source.chromium.org/chromium/chromium/src/+/main:third_party/blink/renderer/core/editing/commands/editor_command_names.h for valid command names.
  """
  @spec dispatch_key_event(
          String.t(),
          integer(),
          CDPotion.Domain.Input.TimeSinceEpoch,
          String.t(),
          String.t(),
          String.t(),
          String.t(),
          String.t(),
          integer(),
          integer(),
          boolean(),
          boolean(),
          boolean(),
          integer(),
          list(String.t())
        ) :: {String.t(), map()}
  def dispatch_key_event(
        type,
        modifiers \\ nil,
        timestamp \\ nil,
        text \\ nil,
        unmodified_text \\ nil,
        key_identifier \\ nil,
        code \\ nil,
        key \\ nil,
        windows_virtual_key_code \\ nil,
        native_virtual_key_code \\ nil,
        auto_repeat \\ nil,
        is_keypad \\ nil,
        is_system_key \\ nil,
        location \\ nil,
        commands \\ nil
      ) do
    params =
      as_query([
        {"type", type},
        {"modifiers", modifiers},
        {"timestamp", timestamp},
        {"text", text},
        {"unmodifiedText", unmodified_text},
        {"keyIdentifier", key_identifier},
        {"code", code},
        {"key", key},
        {"windowsVirtualKeyCode", windows_virtual_key_code},
        {"nativeVirtualKeyCode", native_virtual_key_code},
        {"autoRepeat", auto_repeat},
        {"isKeypad", is_keypad},
        {"isSystemKey", is_system_key},
        {"location", location},
        {"commands", commands}
      ])

    {"Input.dispatchKeyEvent", params}
  end

  @doc """
  This method emulates inserting text that doesn't come from a key press,
  for example an emoji keyboard or an IME.
  ## Parameters:
    - (Required) `text`: The text to insert.
  """
  @spec insert_text(String.t()) :: {String.t(), map()}
  def insert_text(text) do
    params = as_query([{"text", text}])
    {"Input.insertText", params}
  end

  @doc """
  This method sets the current candidate text for ime.
