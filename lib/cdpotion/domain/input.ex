      ])

    {"Input.imeSetComposition", params}
  end

  @doc """
  Dispatches a mouse event to the page.
  ## Parameters:
    - (Required) `type`: Type of the mouse event.
  - (Required) `x`: X coordinate of the event relative to the main frame's viewport in CSS pixels.
  - (Required) `y`: Y coordinate of the event relative to the main frame's viewport in CSS pixels. 0 refers to
  the top of the viewport and Y increases as it proceeds towards the bottom of the viewport.
  - (Optional) `modifiers`: Bit field representing pressed modifier keys. Alt=1, Ctrl=2, Meta/Command=4, Shift=8
  (default: 0).
  - (Optional) `timestamp`: Time at which the event occurred.
  - (Optional) `button`: Mouse button (default: "none").
  - (Optional) `buttons`: A number indicating which buttons are pressed on the mouse when a mouse event is triggered.
  Left=1, Right=2, Middle=4, Back=8, Forward=16, None=0.
  - (Optional) `click_count`: Number of times the mouse button was clicked (default: 0).
  - (Optional) `force`: The normalized pressure, which has a range of [0,1] (default: 0).
  - (Optional) `tangential_pressure`: The normalized tangential pressure, which has a range of [-1,1] (default: 0).
  - (Optional) `tilt_x`: The plane angle between the Y-Z plane and the plane containing both the stylus axis and the Y axis, in degrees of the range [-90,90], a positive tiltX is to the right (default: 0).
  - (Optional) `tilt_y`: The plane angle between the X-Z plane and the plane containing both the stylus axis and the X axis, in degrees of the range [-90,90], a positive tiltY is towards the user (default: 0).
  - (Optional) `twist`: The clockwise rotation of a pen stylus around its own major axis, in degrees in the range [0,359] (default: 0).
  - (Optional) `delta_x`: X delta in CSS pixels for mouse wheel event (default: 0).
  - (Optional) `delta_y`: Y delta in CSS pixels for mouse wheel event (default: 0).
  - (Optional) `pointer_type`: Pointer type (default: "mouse").
  """
  @spec dispatch_mouse_event(
          String.t(),
          number(),
          number(),
          integer(),
          CDPotion.Domain.Input.TimeSinceEpoch,
          CDPotion.Domain.Input.MouseButton,
          integer(),
          integer(),
          number(),
          number(),
          integer(),
          integer(),
          integer(),
          number(),
          number(),
          String.t()
        ) :: {String.t(), map()}
  def dispatch_mouse_event(
        type,
        x,
        y,
        modifiers \\ nil,
        timestamp \\ nil,
        button \\ nil,
        buttons \\ nil,
        click_count \\ nil,
        force \\ nil,
        tangential_pressure \\ nil,
        tilt_x \\ nil,
        tilt_y \\ nil,
        twist \\ nil,
        delta_x \\ nil,
        delta_y \\ nil,
        pointer_type \\ nil
      ) do
    params =
      as_query([
        {"type", type},
        {"x", x},
        {"y", y},
        {"modifiers", modifiers},
        {"timestamp", timestamp},
        {"button", button},
        {"buttons", buttons},
        {"clickCount", click_count},
        {"force", force},
        {"tangentialPressure", tangential_pressure},
        {"tiltX", tilt_x},
        {"tiltY", tilt_y},
        {"twist", twist},
        {"deltaX", delta_x},
        {"deltaY", delta_y},
        {"pointerType", pointer_type}
      ])

    {"Input.dispatchMouseEvent", params}
  end

  @doc """
  Dispatches a touch event to the page.
  ## Parameters:
    - (Required) `type`: Type of the touch event. TouchEnd and TouchCancel must not contain any touch points, while
  TouchStart and TouchMove must contains at least one.
  - (Required) `touch_points`: Active touch points on the touch device. One event per any changed point (compared to
  previous touch event in a sequence) is generated, emulating pressing/moving/releasing points
  one by one.
  - (Optional) `modifiers`: Bit field representing pressed modifier keys. Alt=1, Ctrl=2, Meta/Command=4, Shift=8
  (default: 0).
  - (Optional) `timestamp`: Time at which the event occurred.
  """
  @spec dispatch_touch_event(
          String.t(),
          list(CDPotion.Domain.Input.TouchPoint),
          integer(),
          CDPotion.Domain.Input.TimeSinceEpoch
