  def dispatch_touch_event(type, touch_points, modifiers \\ nil, timestamp \\ nil) do
    params =
      as_query([
        {"type", type},
        {"touchPoints", touch_points},
        {"modifiers", modifiers},
        {"timestamp", timestamp}
      ])

    {"Input.dispatchTouchEvent", params}
  end

